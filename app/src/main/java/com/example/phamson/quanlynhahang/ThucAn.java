package com.example.phamson.quanlynhahang;

public class ThucAn{
    public String name;
    public String number;
    public String price;
    public String hinhAnh;
    public String banMay;

    public ThucAn() {
    }

    public ThucAn(String name, String number, String price, String hinhAnh , String banMay) {
        this.name = name;
        this.number = number;
        this.price = price;
        this.hinhAnh = hinhAnh;
        this.banMay = banMay;
    }
}
